package fundamentos;

public class Operadores {

	public static void main(String[] args) {
		//Aritmeticos
		
		var x = 5.0;
		var y = 2;
		
		System.out.println(x + y);
		System.out.println(x - y);
		System.out.println(x * y);
		System.out.println(x / y);
		System.out.println(x % y);

	}

}
