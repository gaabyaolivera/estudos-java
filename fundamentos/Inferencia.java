package fundamentos;

public class Inferencia {
	public static void main(String[] args) {
		var a = 5.5;
		System.out.println(a);
		
		// aqui ele transforma o 2 em um double
		a = 2;
		System.out.println(a);
		
		var nome = "Gabriella";
		System.out.println(nome);
		
		// não permite atribuir valor de tipo diferente da variável original
		//nome = 2;
		
		var b = 5.5;
		System.out.println(b);
		
		b = 12.5;
		System.out.println(b);
		
		// a variável precisa ser inicializada
		//var e;
	}
}
